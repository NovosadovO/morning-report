    key_asp = _ASP_KEY_MAP.get(aspect_name, "")
    t = _clean_planet(transit_planet)
    n = _clean_planet(natal_planet)
    # Пряма комбінація
    desc = ASPECT_DESCRIPTIONS.get((t, key_asp, n), "")
