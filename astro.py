def get_aspect_description(transit_planet: str, aspect_name: str, natal_planet: str) -> str:
    """Повертає опис аспекту або порожній рядок."""
    key_asp = _ASP_KEY_MAP.get(aspect_name, "")
    # Пряма комбінація
    desc = ASPECT_DESCRIPTIONS.get((transit_planet, key_asp, natal_planet), "")
    if not desc:
        # Зворотна (наприклад Місяць трин Сонце = Сонце трин Місяць)
        desc = ASPECT_DESCRIPTIONS.get((natal_planet, key_asp, transit_planet), "")
    return desc


ASPECTS_UA = {
    0:   ("Кон'юнкція ☌", "🔴", 8),
    60:  ("Секстиль ⚹",   "🟢", 6),
    90:  ("Квадрат □",    "🔴", 8),
    120: ("Трин △",       "🟢", 8),
    150: ("Квінконкс ⚻",  "🟡", 3),
    180: ("Опозиція ☍",   "🔴", 8),
}

# ─── HELPERS ──────────────────────────────────────────────────────────────────

def _get_planet(subject, key):
    return getattr(subject, key, None)

def _sign_ua(sign_key):
    return SIGNS_UA.get(sign_key, sign_key)

def _deg_str(pos):
    d = int(pos)
    m = int((pos % 1) * 60)
    return f"{d}°{m}'"

def _get_natal_house(lon, cusps):
    lon = lon % 360
    for i in range(11, -1, -1):
        cs = cusps[i] % 360
        ce = cusps[(i + 1) % 12] % 360
        if cs > ce:
            if lon >= cs or lon < ce:
                return i + 1
        else:
            if cs <= lon < ce:
                return i + 1
    return 12

def _angle_diff(a, b):
    d = abs(a - b) % 360
    return min(d, 360 - d)

def _moon_phase(sun_lon, moon_lon):
    diff = (moon_lon - sun_lon) % 360
    idx = int(diff / 45)
    return MOON_PHASES_UA[idx], diff

def _get_aspects(lons, names):
    aspects = []
    for i in range(len(lons)):
        for j in range(i + 1, len(lons)):
            diff = _angle_diff(lons[i], lons[j])
            for angle, (asp_name, emoji, orb) in ASPECTS_UA.items():
                if abs(diff - angle) <= orb:
                    aspects.append((names[i], names[j], asp_name, emoji, abs(diff - angle)))
    aspects.sort(key=lambda x: x[4])
    return aspects

def _transit_aspects(natal_lons, transit_lons, natal_names, transit_names, orb=3.0):
    aspects = []
    for ti, t_lon in enumerate(transit_lons):
        for ni, n_lon in enumerate(natal_lons):
            diff = _angle_diff(t_lon, n_lon)
            for angle, (asp_name, emoji, _) in ASPECTS_UA.items():
                if abs(diff - angle) <= orb:
                    aspects.append((transit_names[ti], natal_names[ni], asp_name, emoji, abs(diff - angle)))
    aspects.sort(key=lambda x: x[4])
    return aspects[:12]

